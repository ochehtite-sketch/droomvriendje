export const exportFullCampaignStructure = () => {
  const rows = [
    ["Campaign", "Ad Group", "Keyword", "Match Type", "Max CPC", "Priority"]
  ];
  
  extendedKeywordCategories.forEach(cat => {
    cat.keywords.forEach(keyword => {
      const matchType = cat.matchType === "EXACT_MATCH" ? "Exact" : 
                       cat.matchType === "PHRASE_MATCH" ? "Phrase" : "Broad";
      rows.push([
        "Shopping - Droomvriendjes",
        cat.adGroup || cat.name,
        keyword,
        matchType,
        cat.suggestedBid,
        cat.priority || "MEDIUM"
      ]);
    });
  });
  
  return rows.map(row => row.join(",")).join("\n");
};

